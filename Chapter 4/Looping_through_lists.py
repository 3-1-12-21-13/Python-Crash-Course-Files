# LOOPING THROUGH AN ENTIRE LIST
# You can use a for loop like below to repeat an action for all values within
# a list. 
# Example below:
numbers = [1,2,3,4,5,6]
# The line below tells Python to pull a value from the list 'numbers' and 
# associate it with the variable 'number'. We then tell Python to print a
# short statement followed by the number assigned to the 'number' variable.
for number in numbers:
    print(f"This is a number in the list - {number}")
# It can be easier to think of it like - For every number in the list of
# numbers, print that number after the statement.