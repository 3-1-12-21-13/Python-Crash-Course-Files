# TUPLES
# Sometimes you will want to create a list of items where the values cannot
# change. Python refers to values which cannot change as immutable. A list of
# Immutable items is called a tuple.
# Example below:
Dimensions = (200,50)
print(Dimensions[0])
print(Dimensions[1])
# Tuples are technically defined by the presence of a comma; the parantheses
# Make them look neater and more readable. If you want to define a tuple with
# one element, you need to include a trailing comma.