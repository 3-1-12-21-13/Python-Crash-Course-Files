# SIMPLE STATISTICS WITH LIST OF NUMBERS
# A few Python functions are helpful when working with lists of numbers
# You can easily find the minimum, maximum and sum of a list of numbers:
numbers = list(range(4,93,5))
print(f"The minimum value in my list is - {min(numbers)}.")
print(f"The maximum value in my list is - {max(numbers)}.")
print(f"The sum of all my values is - {sum(numbers)}.")