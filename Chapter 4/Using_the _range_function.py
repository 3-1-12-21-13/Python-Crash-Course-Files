# USING THE RANGE() FUNCTION
# The range() function makes it easy to generate a series of numbers. For
# example, you can use the range function to print a series of numbers.

for value in range(1,5):
    print(value)

# Python stops at that second value, the output never contains the end value, 
# which would have been 5 in this case.
# You can also pass range() only one argument, and it will start the sequence
# of numbers at 0. For example, range(6) would return the numbers from 0 
# through 5.

