# Underscores in Numbers
# When you print a number that was defined using underscores, Python prints only the digits:
universe_age = 14_000_000_000
print(universe_age)

# Multiple Assignment
# You can assign values to more than one variable using a single line. This can help shorten your
# programs  and make them easier to read; You will use this technique most often when initializing a set of
# numbers.
# Example below:
x,y,z = 0,0,0
print(x)
print(y)
print(z)

# Constants
# A constant is a variable whose value stays the sme throughout the life of a program. Python does not have
# built in constant types, but uses all capital letters to indicate a variable should be treated as a constant
# and never be changed.
# example below:
MAX_CONNECTIONS = 5000

