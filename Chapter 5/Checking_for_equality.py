# CHECKING FOR EQUALITY
# Within every if statement is an expression that can be evaluated as true or false and is called a conditional test.
# Python uses the values of true or false to decide whether the code in an if statement should be executed. If true, then Python will execute the code, If the test evaluates to false, Python will ignore it.

# Most conditional tests compare the current value of a variable to a specific value of interest. The simplest conditional test checks whether the value of a variable is equal to the value of interest:
# Example below:
my_number = 1209310841.222
print(my_number == 1209310841.222)

# The equality operater (==) returns TRUE if the values on the left and right side of the operator match, and FALSE if they don't match. 