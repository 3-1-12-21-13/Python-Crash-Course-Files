# NUMBERICAL COMPARISONS
# With Python, you can check if two numbers are not equal pretty easily. Example below:
number = 47
if number != 21:
    print("This number is not 47.")

# This conditional test passes because the value of number is not equal to 47.
# You can include various mathematical comparisons in your conditional statements as well such as less than, less than or equal to greater than or equal to:
age = 30
print(f"The age is  - {age}")
if age < 40:
    print ("The age is below 40.")
if age <= 30:
    print ("The age is equal to or below 30.")
if age > 30:
    print ("The age is greater than 30.")
if age >= 20:
    print ("The age is equal to or above 20.")