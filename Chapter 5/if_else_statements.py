# IF-ELSE STATEMENTS
# When you want to take one action if a conditional test passes and a different action in all other cases, you can use the IF-ELSE syntax. 
# An IF-ELSE block is similar to a simple IF statement, but the ELSE statement allows you to define an action or set of actions that are executed when the conditional test fails.
# Example below:
number = 23
if number < 20:
    print("The number is lower than 20.")
else:
    print("The number is higher than 20.")